#define STOP ((void*)0)

int	ft_any(char **tab, int (*f)(char*))
{
	int res;
	int i;

	res = 1;
	i = 0;
	while (tab[i] != STOP)
	{
		res = f(tab[i]);
		if (res != 0)
			return (1);
		i++;
	}
	return (0);
}
