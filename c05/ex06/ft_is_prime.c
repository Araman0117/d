int	ft_is_prime(int nb)
{
	int div;

	div = 2;
	while ((div < nb) && (nb > 0))
	{
		if (nb % div == 0)
			return (0);
		div++;
	}
	if (nb < 2)
		return (0);
	return (1);
}
